// The deploy pipeline: agent config in, live Convex deployment out.
//
//   materialize  copy apps/agent + packages/backend templates into a scratch
//                workspace, apply the config (model, instructions, tools,
//                schedule, crons)
//   build        `eve build` the agent, vendor the bundle into the backend copy
//   provision    create a fresh Convex project (or reuse on redeploy) and push
//   configure    set deployment env vars (world secret, queue namespace, keys)
//   bundle       upload the eve bundle into Convex storage (runner hot-swaps)
//   web          vite-build the dashboard against the new deployment + upload
//   verify       runner probe + static site check
//
// Uses the Convex CLI (authenticated via ~/.convex) for provisioning/pushes —
// the same operations the Management API exposes for platform accounts.

